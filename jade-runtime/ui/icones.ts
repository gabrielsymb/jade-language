/**
 * Cria um elemento SVG a partir de um nome de ícone do catálogo.
 * Retorna `null` se o nome não existir — nunca lança exceção.
 *
 * @param nome   Nome em português do ícone (ex: 'casa', 'usuario', 'excluir')
 * @param tamanho Tamanho em pixels (aplicado em width e height). Padrão: 20
 */
export function criarElementoIcone(nome: string, tamanho = 20): SVGSVGElement | null {
  const conteudo = CATALOGO[nome.toLowerCase()];
  if (!conteudo) return null;

  const svg = document.createElementNS('http://www.w3.org/2000/svg', 'svg');
  svg.setAttribute('viewBox', '0 0 24 24');
  svg.setAttribute('width', String(tamanho));
  svg.setAttribute('height', String(tamanho));
  svg.setAttribute('fill', 'none');
  svg.setAttribute('stroke', 'currentColor');
  svg.setAttribute('stroke-width', '2');
  svg.setAttribute('stroke-linecap', 'round');
  svg.setAttribute('stroke-linejoin', 'round');
  svg.setAttribute('aria-hidden', 'true');
  svg.innerHTML = conteudo;
  return svg;
}

/** Lista todos os nomes de ícones disponíveis (útil para debug/docs). */
export function listarIcones(): string[] {
  return Object.keys(CATALOGO).sort();
}
